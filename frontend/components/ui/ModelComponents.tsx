export const ModelBadge: React.FC<ModelBadgeProps> = ({
  type,
  size = 16,
  className = "",
}) => {
  const badgeStyles = cn(
    "inline-flex items-center gap-1.5 px-2 py-1 rounded-md text-xs font-medium transition-colors",
    {
      "text-orange-700": type === "premium",
      "text-blue-700": type === "reasoning",
    },
    className
  );

  const renderIcon = () => {
    switch (type) {
      case "premium":
        return <DiamondIcon size={size} />;
      case "reasoning":
        return <BrainIcon size={size} />;
      default:
        return null;
    }
  };

  const renderText = () => {
    switch (type) {
      case "premium":
        return "Premium";
      case "reasoning":
        return "Reasoning";
      default:
        return "";
    }
  };

  return (
    <span className={badgeStyles}>
      {renderIcon()}
      {/* {renderText()} */}
    </span>
  );
};

